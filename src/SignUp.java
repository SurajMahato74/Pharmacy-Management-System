    private void jToggleButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jToggleButton1ActionPerformed
        // TODO add your handling code here:
        String userRole = (String) jComboBox1.getSelectedItem();
        String name = txtNme.getText();
        SimpleDateFormat dFormat = new SimpleDateFormat("dd-MM-yyyy");
        Date date = dateDB.getDate();
        String dob = "";
        if (date != null) {
            dob = dFormat.format(dateDB.getDate());

        }
        String mobileNumber = txtNum.getText();
        String email = txtEml.getText();
        String username = txtUsName.getText();
        String password = jTextField6.getText();
        String address = txtAddres.getText();
        String status = "Pending"; // Set the initial status for new users

        if (name.equals("")) {
            JOptionPane.showMessageDialog(null, "name field is required.");
        } else if (dob.equals("")) {
            JOptionPane.showMessageDialog(null, "Date of Birth field is required.");
        } else if (mobileNumber.equals("")) {
            JOptionPane.showMessageDialog(null, "Mobile Number field is required");
        } else if (!mobileNumber.matches(mobileNumberPatern) || mobileNumber.length() != 10) {
            JOptionPane.showMessageDialog(null, "Mobile number field is invalid");
        } else if (email.equals("")) {
            JOptionPane.showMessageDialog(null, "Email field is required");
        } else if (!email.matches(emailPattern)) {
            JOptionPane.showMessageDialog(null, "Email field is invalid");
        } else if (username.equals("")) {
            JOptionPane.showMessageDialog(null, "Username field is required");
        } else if (checkUsername == 1) {
            JOptionPane.showMessageDialog(null, "Username already exist.");
        } else if (password.equals("")) {
            JOptionPane.showMessageDialog(null, "Password field is required");
        } else if (address.equals("")) {
            JOptionPane.showMessageDialog(null, "Address field is required");
        } else {
            try {
                Connection con = ConnectionProvider.getCon();
                PreparedStatement ps = con.prepareStatement("insert into appuser (userRole,name,dob,mobileNumber,email,username,password,address,status) values(?,?,?,?,?,?,?,?,?)");
                ps.setString(1, userRole);
                ps.setString(2, name);
                ps.setString(3, dob);
                ps.setString(4, mobileNumber);
                ps.setString(5, email);
                ps.setString(6, username);
                ps.setString(7, password);
                ps.setString(8, address);
                ps.setString(9, status);
                ps.executeUpdate();
                JOptionPane.showMessageDialog(null, "User registration submitted for approval.");
                setVisible(false);
                new SignUp().setVisible(true);

            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            }
        }
    }//GEN-LAST:event_jToggleButton1ActionPerformed

