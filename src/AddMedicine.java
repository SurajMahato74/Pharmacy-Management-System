    private void txtUniqueIdKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtUniqueIdKeyReleased
        // TODO add your handling code here:
        String username = txtUniqueId.getText();
        if(!username.equals("")){
            jLabel7.setVisible(true);
            jLabel7.setIcon(new ImageIcon("src\\images\\yes.png"));
            jLabel7.setText("");
            checkUsername = 0;
            
            try{
            Connection con= ConnectionProvider.getCon();
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("Select *from medicine where uniqueId='"+username+"'");
            while(rs.next()){
                checkUsername = 1;
                jLabel7.setIcon(new ImageIcon("src\\images\\no.png"));
                jLabel7.setText("");
                
            }
            }
            catch(Exception e){
                JOptionPane.showMessageDialog(null,e);
            }
        }
        else{
            jLabel7.setVisible(false);
        }
    }//GEN-LAST:event_txtUniqueIdKeyReleased

