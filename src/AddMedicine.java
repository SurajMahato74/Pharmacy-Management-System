    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String type = (String) comboType.getSelectedItem();
        SimpleDateFormat dFormat = new SimpleDateFormat("yyyy-MM-dd");
        java.util.Date mfdate = DateChooser1.getDate();
        String mfg = "";
        if (mfdate != null) {
            mfg = dFormat.format(DateChooser1.getDate());

        }
        java.util.Date exdate = DateChooser2.getDate();
        String exp = "";
        if (exdate != null) {
            exp = dFormat.format(DateChooser2.getDate());
        }
        
        String uniqueId = txtUniqueId.getText();
        String name = txtName.getText();
        String companyname = txtCompanyName.getText();
        String quantity = txtQuantity.getText();
        String price = txtPricePerUnit1.getText();
        String supplier = txtSupplier.getText();
        String location = txtLocation.getText();

        if (uniqueId.equals("")) {
            JOptionPane.showMessageDialog(null, "Medicine ID is required");

        } else if (name.equals("")) {
            JOptionPane.showMessageDialog(null, "Name Field is required");
        } else if (companyname.equals("")) {
            JOptionPane.showMessageDialog(null, "Company Name is Required");
        } else if (quantity.equals("")) {
            JOptionPane.showMessageDialog(null, "Quantity is required");
        }else if (supplier.equals("")) {
            JOptionPane.showMessageDialog(null, "Supplier is required"); 
        }else if(mfg.equals("")){
            JOptionPane.showMessageDialog(null,"Manifracture Date is required.");
        }else if(exp.equals("")){
            JOptionPane.showMessageDialog(null,"Expiry Date is required.");
        }else if(type.equals("")){
            JOptionPane.showMessageDialog(null,"Date of Birth field is required.");
        }else if (location.equals("")) {
            JOptionPane.showMessageDialog(null, "Location is required");
        }else if (!quantity.matches(numberPattern)) {
            JOptionPane.showMessageDialog(null, "Quntity Field is required");
        } else if (price.equals("")) {
            JOptionPane.showMessageDialog(null, "Price Is required");
        } else if (!price.matches(numberPattern)) {
            JOptionPane.showMessageDialog(null, "Price Field Is invalid.");
        } else {
            try {
                Connection con = ConnectionProvider.getCon();
                PreparedStatement ps = con.prepareStatement("insert into medicine (uniqueId,name,companyName,quantity,price,supplier,mfg,exp,type,location) values(?,?,?,?,?,?,?,?,?,?)");
                ps.setString(1, uniqueId);
                ps.setString(2, name);
                ps.setString(3, companyname);
                ps.setString(4, quantity);
                ps.setString(5, price);
                ps.setString(6, supplier);
                ps.setString(7, mfg);
                ps.setString(8, exp);
                ps.setString(9, type);
                ps.setString(10, location);
                ps.executeUpdate();
                JOptionPane.showMessageDialog(null, "Medicine Added Sucessfully");
                setVisible(false);
                new AddMedicine().setVisible(true);
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            }


    }//GEN-LAST:event_jButton2ActionPerformed
    }
